# Boolean: Can have the value True or False
x_bool = True

# Integer: Whole numbers. Can be positive or negative.
x_int = 1

# Float: Decimal numbers. Can be positive or negative.
x_float = 2.3678

# String: Contains text.
x_string = "Hello!"

# Print function outputs variables or other text so that you can see what your code is doing.
print(50 * "-")
print(x_bool)
print(x_int)
print(x_float)
print(x_string)
print(50 * "-")

name = "John"
age = 43

# f-strings allow you to place variables within a string.
print(f"{name} is {age} years old.")
print(50 * "-")

x = 5
y = 2

z_add = x + y  # Addition
z_sub = x - y  # Subtraction
z_mul = x * y  # Multiplication
z_div = x / y  # Division
z_pow = x ** y  # Exponentiation (i.e., x to the power of y)
z_mod = x % y  # Modulo (i.e., x divided by y equals quotient remainder z_mod)

print(f"{x} + {y}  = {z_add}")
print(f"{x} - {y}  = {z_sub}")
print(f"{x} * {y}  = {z_mul}")
print(f"{x} / {y}  = {z_div}")
print(f"{x} ** {y} = {z_pow}")
print(f"{x} % {y}  = {z_mod}")
print(50 * "-")
